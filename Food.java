public class Food
{
    private int x;
    private int y;
    public Food(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
    public int x()
    {
        return x;
    }
    public int y()
    {
        return y;
    }
    public void setX(int y)
    {
        x = y;
    }
    public void setY(int x)
    {
        y = x;
    }
}